    private void jButtonPrijavaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPrijavaActionPerformed
        // TODO add your handling code here:
        brojac++;
        int preostaliBroj = max_broj_pok - brojac;
        String error = "";
        if(jTextFieldEmail.getText() == null || jTextFieldEmail.getText().isEmpty()){
            error += "Morate uneti email \n";
        }
        if(String.valueOf(jPasswordField1.getPassword()) == null || String.valueOf(jPasswordField1.getPassword()).isEmpty()){
            error += "Morate uneti password \n";
        }
        if(!error.isEmpty()){
            JOptionPane.showMessageDialog(this, error + " Imate jos " + preostaliBroj + " pokusaja" , "GRESKA", JOptionPane.ERROR_MESSAGE);
            if(preostaliBroj == 0){
                JOptionPane.showMessageDialog(this, "Korisnik nije pronadjen u sistemu! Nemate vise pokusaja! " , "GRESKA", JOptionPane.ERROR_MESSAGE);
                return;
            }
            return;
        }
        if(controller.postojiKorisnik(jTextFieldEmail.getText(), String.valueOf(jPasswordField1.getPassword()))){
            JOptionPane.showMessageDialog(this, "USPESNA PRIJAVA" , "USPESNO", JOptionPane.INFORMATION_MESSAGE);
            new UnosProfesoraFrm().setVisible(true);
            return;
        }else{
            JOptionPane.showMessageDialog(this, "Korisnik nije pronadjen u sistemu! Imate jos " + preostaliBroj + " pokusaja" , "GRESKA", JOptionPane.ERROR_MESSAGE);
            if(preostaliBroj == 0){
                return;
            }
        }
        if(preostaliBroj == 0){
            JOptionPane.showMessageDialog(this, "Korisnik nije pronadjen u sistemu! Nemate vise pokusaja! " , "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
    }//GEN-LAST:event_jButtonPrijavaActionPerformed

